    Write-Error "Python 3.10+ ?????????????B?C???X?g?[???? pip install -r manual/requirements.txt ?????s????????????B"
}

$pubArgs = @(
    "scripts\manual_publish.py",
    "--manifest", $Manifest
)
if ($SkipDataPrep) { $pubArgs += "--skip-data-prep" }
if ($SkipSnap) { $pubArgs += "--skip-snap" }
if ($SkipSync) { $pubArgs += "--skip-sync" }
if ($SkipInject) { $pubArgs += "--skip-inject" }
if ($SkipHtml) { $pubArgs += "--skip-html" }

Write-Host "Using $py $($pubArgs -join ' ')"
& $py @pubArgs
exit $LASTEXITCODE
